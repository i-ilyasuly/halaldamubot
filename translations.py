    """
    Аударма функциясы.
    Қолдану: t('welcome_back', lang, name='Айбек')
    """
    entry = TEXTS.get(key, {})
    text = entry.get(lang) or entry.get('kz') or f"[{key}]"
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
    return text
